public class NumberSystem {
    public static void main (String [] args) {
        int num = 241;

        System.out.printf("Decimal    : %d\n", num);
        System.out.printf("Octal      : %o\n", num);
        System.out.printf("Hexadecimal: %x\n", num);
        System.out.printf("Hexadecimal: %X\n", num);
    }
}